private void dataChoose(){
	int row = tableTypeItem.getSelectedRow();
	
	typeItems.setCodeType(tableTypeItem.getModel().getValueAt(row, 1).toString());
	typeItems.setNameType(tableTypeItem.getModel().getValueAt(row, 2).toString());
	dispose();
}

