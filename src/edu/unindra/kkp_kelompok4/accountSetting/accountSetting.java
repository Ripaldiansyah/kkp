private void saveProfile() {
    modelAccountSetting accountRenew = new modelAccountSetting();
    String id = account.getIdEmployee();
    String name = jtNameEmployee1.getText();
    String gender = "";
    if (male.isSelected()) {
        gender = male.getText();
    } else if (female.isSelected()) {
        gender = female.getText();
    }
    String placeOfBirth = jtPlaceOfBirth.getText();
    java.util.Date dob = dateOfBirthField.getDate();
    java.sql.Date dateOfBirthConvert = new java.sql.Date(dob.getTime());
    String address = tfAddress.getText();
    String email = jtEmail.getText() + jtGmail.getText();
    String phoneNumberEmployee = jtPhoneNumber.getText();
    Icon pictures = choosePic2.getIcon();

    accountRenew.setIdEmployee(id);
    accountRenew.setEmployeeName(name);
    accountRenew.setGender(gender);
    accountRenew.setPlaceOfBirth(placeOfBirth);
    accountRenew.setDateOfBirth(dateOfBirthConvert);
    accountRenew.setAddress(address);
    accountRenew.setEmail(email);
    accountRenew.setPhoneNumber(phoneNumberEmployee);

    if (pictures instanceof ImageIcon) {
        ImageIcon imageIcon = (ImageIcon) pictures;
        Image image = imageIcon.getImage();

        BufferedImage bufferedImage = new BufferedImage(image.getWidth(null), image.getHeight(null), BufferedImage.TYPE_INT_RGB);
        Graphics2D g2d = bufferedImage.createGraphics();
        g2d.drawImage(image, 0, 0, null);
        g2d.dispose();

        try (ByteArrayOutputStream baos = new ByteArrayOutputStream()) {
            ImageIO.write(bufferedImage, "JPEG", baos);
            byte[] picture = baos.toByteArray();
            accountRenew.setPictures(picture);
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

    accountSetting.profileSetting(accountRenew);
    
    panelDisplay();
    accountInfo();
}


private void changePassword() {
    accountSetting.displayAccount(account);
    String passwordOldCheck = account.getPassword();
    String oldPass = encryptLogin.getMD5(oldPassword.getText());
    String newPass = newPassword.getText();
    String newConfirmationPassword = confirmationNewPassword.getText();
    
    if (oldPass.equals(passwordOldCheck)) {
        if (newPass.equals(newConfirmationPassword)) {
            account.setPassword(encryptLogin.getMD5(newPass));
            accountSetting.passwordChange(account);
            panelDisplay();
            accountInfo();
        } else {
            JOptionPane.showMessageDialog(null, "Password Baru tidak sesuai");
        }
    } else {
        JOptionPane.showMessageDialog(null, "Password tidak sesuai dengan yang terdaftar di database");
    }
}

