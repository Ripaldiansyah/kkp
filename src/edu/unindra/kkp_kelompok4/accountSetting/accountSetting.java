private void changePicture() {
    JFileChooser fileChooser = new JFileChooser();
    FileNameExtensionFilter imageFilter = new FileNameExtensionFilter("Image Files", "jpg", "jpeg", "png", "gif");
    fileChooser.setFileFilter(imageFilter);
    int result = fileChooser.showOpenDialog(null);

    if (result == JFileChooser.APPROVE_OPTION) {
        File selectedFile = fileChooser.getSelectedFile();

        try {
            if (selectedFile != null) {
                ImageIcon img = new ImageIcon(selectedFile.getPath());

                // Get the original image dimensions
                int pictureWidth = img.getIconWidth();
                int pictureHeight = img.getIconHeight();

                // Mengubah ukuran gambar sesuai kebutuhan
                int labelWidth = 230;
                int labelHeight = 150;
                double scaleX = (double) labelWidth / (double) pictureWidth;
                double scaleY = (double) labelHeight / (double) pictureHeight;
                double scale = Math.min(scaleX, scaleY);

                // Scale the image
                int scaledWidth = (int) (scale * pictureWidth);
                int scaledHeight = (int) (scale * pictureHeight);
                Image scaledPicture = img.getImage().getScaledInstance(scaledWidth, scaledHeight, Image.SCALE_SMOOTH);

                BufferedImage bufferedImage = new BufferedImage(scaledWidth, scaledHeight, BufferedImage.TYPE_INT_RGB);

                // Draw the scaled picture onto the BufferedImage
                Graphics2D g2d = bufferedImage.createGraphics();
                g2d.drawImage(scaledPicture, 0, 0, null);
                g2d.dispose();

                // Write the BufferedImage to a ByteArrayOutputStream
                ByteArrayOutputStream baos = new ByteArrayOutputStream();
                ImageIO.write(bufferedImage, "JPEG", baos);
                byte[] picture = baos.toByteArray();
                baos.close();

                account.setPictures(picture);
                choosePic2.setIcon(new ImageIcon(scaledPicture));
            } else {
		byte[]picture=account.getPictures();
                ImageIcon img = new ImageIcon(picture);
                choosePic2.setIcon(img);
                account.setPictures(picture);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
}


